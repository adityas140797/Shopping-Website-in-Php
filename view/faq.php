<h2>
<font color="blue">
How can I track the delivery of my order? </h2>
</font>
All items sold by sellers on Shopstore are delivered through reputed courier partners including Ekart Logistics - Shopstore's preferred delivery partner. You will be provided a Tracking ID for your order by the respective courier service provider. Order IDs contain tracking information from E-Kart Logistics.

<h2>
<font color="blue">
What are the delivery charges? </h2>
</font>
Delivery charge varies with each Seller.
Sellers incur relatively higher shipping costs on low value items. In such cases, charging a nominal delivery charge helps them offset logistics costs. The delivery charge may be waived off by some Sellers, if you shop with them for a minimum predefined value. 

For example, seller WS Retail, which generally charges ₹ 40 for delivery, provides free delivery on orders valued at ₹ 500 or more.

